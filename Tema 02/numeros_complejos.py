# Números complejos

# Definición de un número complejo
# Un número complejo es un número que puede ser expresado en 
# la forma a + bi,
# donde a y b son números reales, e i es la unidad imaginaria,
# que satisface la condición i^2 = -1.

# En Python, los números complejos se pueden definir utilizando
# la función complex() o utilizando la letra j para representar
# la parte imaginaria.

# Ejemplo:
a = 2 + 3j
b = complex(2, 3)

print(a)  # (2+3j)
print(b)  # (2+3j)

# Operaciones con números complejos
# Las operaciones básicas que se pueden realizar con números
# complejos son la suma, la resta, la multiplicación y la división.

# Ejemplo:
a = 2 + 3j
b = 1 + 2j